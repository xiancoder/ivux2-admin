// 制作正则表达式
function createRegexp (op) {
    let args = Array.prototype.slice.call(arguments, 0)
    op = op || ''
    let regText = args.join('')
    return {
        g: new RegExp(regText, 'g'),
        gi: new RegExp(regText, 'gi'),
        i: new RegExp(regText, 'i'),
        no: new RegExp(regText, '')
    }
}

